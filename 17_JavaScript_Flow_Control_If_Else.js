let a = 10;
let b = 20;
let result;

if (a < b){
    result = "a is smaller";
}else {
    result = "a is not smaller";
}


console.log(result);


///An example of ternary operators as well

let a = 10;
let b = 20;
let result;

result = a < b ? 'a is smaller':'a is not smaller'; 

console.log(result);

